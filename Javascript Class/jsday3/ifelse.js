let color = prompt("What is the color of your dress?");
if(color == "white"){
    alert("White it is");
}
else if(color == "Blue"){
    alert("Blue it is");
}
else if(color == "Green"){
    alert("Green it is");
}
else if(color == "Yellow"){
    alert("Yellow it is");
}
else{
    alert(`${color} it is`);
}